package visualization;

/**
 *
 * @author Godhani
 */
public class Author {
    Integer id;
    String name;
    String gscholaruserid;
    String created;
    String modified;
    
    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGscholaruserid() {
        return gscholaruserid;
    }

    public void setGscholaruserid(String gscholaruserid) {
        this.gscholaruserid = gscholaruserid;
    }

    public String getCreated() {
        return created;
    }

    public void setCreated(String created) {
        this.created = created;
    }

    public String getModified() {
        return modified;
    }

    public void setModified(String modified) {
        this.modified = modified;
    }
    
    
}
